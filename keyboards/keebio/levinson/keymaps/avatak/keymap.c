void encoder_update_user(uint8_t index, bool clockwise) {
    if (index == 0) { /* First encoder */
        if (IS_LAYER_ON(_NAV)|IS_LAYER_ON(_MNAV)) {
            if (clockwise) {
                tap_code(KC_TAB);
            } else {
                register_code(KC_LSFT);
                tap_code(KC_TAB);
                unregister_code(KC_LSFT);
            }
        }
        else if (IS_LAYER_ON(_TEX)) {
            if (clockwise) {
                register_code(KC_LSFT);
                register_code(KC_LCTL);
                tap_code(KC_Z);
                unregister_code(KC_LCTL);
                unregister_code(KC_LSFT);
            } else {
                register_code(KC_LCTL);
                tap_code(KC_Z);
                unregister_code(KC_LCTL);
            }
        }
        else if (IS_LAYER_ON(_SYM)) {
            if (clockwise) {
                tap_code(KC_DEL);
            } else {
                tap_code(KC_BSPC);
            }
        }
        else if (IS_LAYER_ON(_ADJUST)) {
            if (clockwise) {
                rgblight_increase_hue();
            } else {
                rgblight_decrease_hue();
            }
        }
        else if (IS_LAYER_ON(_MEDIA)) {
            if (clockwise) {
                tap_code(KC_VOLU);
            } else {
                tap_code(KC_VOLD);
            }
        }
        else
            if (clockwise) {
                tap_code(KC_DOWN);
            } else {
                tap_code(KC_UP);
            }
    }
// Second Encoder
    else if (index == 1) {
        if (IS_LAYER_ON(_NAV)|IS_LAYER_ON(_MNAV)) {
            if (clockwise) {
                tap_code(KC_PGDN);
            } else {
                tap_code(KC_PGUP);
            }
        }
        else if (IS_LAYER_ON(_ADJUST)) {
            if (clockwise) {
                rgblight_step();
            } else {
                rgblight_step_reverse();
            }
        }
        else if (IS_LAYER_ON(_SYM)) {
            if (clockwise) {
                tap_code(KC_DEL);
            } else {
                tap_code(KC_BSPC);
            }
        }
        else {
            if (clockwise) {
                tap_code(KC_RIGHT);
            } else {
                tap_code(KC_LEFT);
            }
        }
    }
  }
